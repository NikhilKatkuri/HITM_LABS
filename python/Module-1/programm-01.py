# Read the temperature in centigrade and convert it into Fahrenheit

centigrade = float(input("Enter temperature in centigrade: "))
fahrenheit = (centigrade * 9/5) + 32
print("Temperature in Fahrenheit:", fahrenheit)

# output:

# Enter temperature in centigrade: 25
# Temperature in Fahrenheit: 77.0

# Enter temperature in centigrade: 0
# Temperature in Fahrenheit: 32.0